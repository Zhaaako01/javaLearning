package lesson15;

public class Test5 {
    public static void main(String[] args) {
        int a = 15;
        // минимальная реализация while loop-a
        while (a < 10) ;  // условие = false, программа никогда не сработает
        // infinitive while (бесконечный цикл while)
        while (a > 10) ;  // условие = true, программа никогда не закончится
                          // и то что после while loop-a никогда не сработает
    }
}
